#ifndef __JY901_H__
#define __JY901_H__

#include "main.h"
#include "i2c.h"   // 引入 i2c.h 以便使用 hi2c2 句柄

//=============================================================================
// 配置区域
//=============================================================================

// JY-901 I2C 从设备地址 (7位地址 0x50，HAL库要求左移一位变为 8位地址)
#define JY901_SLAVE_ADDRESS    (0x50 << 1) 

// JY-901 寄存器地址
#define JY901_REG_CONFIG_SLEEP    0x22  // 休眠控制寄存器
#define JY901_REG_GYRO_Z_L        0x39  // Z轴角速度 低字节 (WzL)
#define JY901_REG_ANGLE_YAW_L     0x3F  // Z轴角度 (偏航角) 低字节 (YawL)

//=============================================================================
// 函数原型声明
//=============================================================================

/**
 * @brief  发送唤醒JY-901或使其保持工作状态的指令.
 * @retval 0: 成功, -1: 失败
 */
int8_t JY901_WakeUp(void);

/**
 * @brief  从JY-901读取Z轴角速度.
 * @param  gyro_z_dps: 存储读取到的Z轴角速度值 (单位: 度/秒).
 * @retval 0: 成功, -1: 失败
 */
int8_t JY901_Read_Gyro_Z(float* gyro_z_dps);

/**
 * @brief  从JY-901读取Yaw轴角度 (偏航角).
 * @param  yaw_deg: 存储读取到的Yaw角度值 (单位: 度).
 * @retval 0: 成功, -1: 失败
 */
int8_t JY901_Read_Yaw(float* yaw_deg);

#endif // __JY901_H__